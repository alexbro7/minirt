#include "../includes/minirt.h"

int		is_charnb(char *s, int i)
{
	if (is_numeric(s[i]) || s[i] == ' ' || s[i] == '\t' || s[i] == ','
		|| (s[i] == '-' && is_numeric(s[i + 1]))
		|| (s[i] == '.' && i > 0 && is_numeric(s[i - 1])))
		return (1);
	else
		return (0);
}
