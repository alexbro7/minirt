#include "../includes/minirt.h"

t_color	addcolor(t_color a, t_color b)
{
	a.r += b.r;
	a.r = (a.r > 255 ? 255 : a.r);
	a.g += b.g;
	a.g = (a.g > 255 ? 255 : a.g);
	a.b += b.b;
	a.b = (a.b > 255 ? 255 : a.b);
	return (a);
}
