#include "../includes/minirt.h"

t_light		lst_lx(t_list *lst, int x)
{
	while (lst && x)
	{
		if (lst->type == 3)
			x--;
		if (!(lst->type == 3 && !x))
			lst = lst->next;
	}
	return (lst->ctt.l);
}
