#include "../includes/minirt.h"

int	is_vector(char *s, int *i)
{
	*i += (s[*i] == '-' ? 1 : 0);
	if (!(is_double(s, i)))
		return (0);
	if (s[*i] != ',')
		return (0);
	*i += 1;
	*i += (s[*i] == '-' ? 1 : 0);
	if (!(is_double(s, i)))
		return (0);
	if (s[*i] != ',')
		return (0);
	*i += 1;
	*i += (s[*i] == '-' ? 1 : 0);
	if (!(is_double(s, i)))
		return (0);
	return (1);
}
