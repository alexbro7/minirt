#include "../includes/minirt.h"

t_d_vector	clr_to_d(t_color c)
{
	t_d_vector	r;

	r.x = (double)c.r * 0.0039215686274509803;
	r.y = (double)c.g * 0.0039215686274509803;
	r.z = (double)c.b * 0.0039215686274509803;
	return (r);
}
