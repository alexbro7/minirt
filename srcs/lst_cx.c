#include "../includes/minirt.h"

t_camera		lst_cx(t_list *lst, int x)
{
	while (lst && x)
	{
		if (lst->type == 2)
			x--;
		if (x)
			lst = lst->next;
	}
	return (lst->ctt.cmr);
}
